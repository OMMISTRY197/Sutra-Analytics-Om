import math

print('ceiling:',math.ceil(2.7)) #return the upper roundfigure value
print('floor:',math.floor(2.7))  #return the lower roundfigure value
print(math.fabs(-5)) #return the positive value of any integer value
print(math.factorial(6)) #return factorial 6*5*4*3*2*1
print(math.sqrt(16)) #return square root
print(math.pi) #return the pi value
print(math.tau) #ratio of the circumference to the radius of a circle
print(math.gcd(10,20)) #greatest common divisor
print(math.pow(2,5))   #power find kare - 2*2*2*2*2
print(math.fmod(5,10)) #Returns the remainder when x is divided by y
print(math.copysign(5,-8)) #Returns the number with the value of ‘x’ but with the sign of ‘y’